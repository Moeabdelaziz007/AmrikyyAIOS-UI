export type Language = keyof typeof translations;

export type TranslationPlaceholder = { [key: string]: string | number };
// Fallback to English keys if a translation is missing
export type TranslationKey = keyof (typeof translations)['en'];

export const getTranslator = (lang: Language) => {
  return (key: TranslationKey, placeholders?: TranslationPlaceholder): string => {
    let translation = translations[lang]?.[key] || translations['en'][key];
    if (placeholders) {
        Object.entries(placeholders).forEach(([pkey, value]) => {
            translation = translation.replace(`{${pkey}}`, String(value));
        });
    }
    return translation;
  };
};